package model;

public class Game 
{
	private int row;
	private int column;
	private String counter;

	public int getRow() 
	{
		return row;
	}

	public void setRow(int row) 
	{
		this.row = row;
	}

	public int getColumn() 
	{
		return column;
	}

	public void setColumn(int column) 
	{
		this.column = column;
	}

	public String getCounter() 
	{
		return counter;
	}

	public void setCounter(String s) 
	{
		this.counter = s;
	}	
}
